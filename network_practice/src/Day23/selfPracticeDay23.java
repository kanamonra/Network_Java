// This program has a triple sorting algorithm for students name, age, GPA

public class selfPracticeDay23 {
    private String name;
    private int GPA;
    private int age;
    public selfPracticeDay23(String name, int GPA, int age){
        this.name = name;
        this.GPA = GPA;
        this.age = age;
    }
    public String getName(){ return name;}
    public int getGPA() { return GPA;}
    public int getAge() { return age;}


}
